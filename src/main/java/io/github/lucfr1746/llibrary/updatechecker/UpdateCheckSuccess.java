package io.github.lucfr1746.llibrary.updatechecker;

/**
 * Represents the result of an update check.
 * This enum indicates whether the update check was successful or failed.
 */
public enum UpdateCheckSuccess {

    /**
     * Indicates that the update check was successful.
     */
    SUCCESS,

    /**
     * Indicates that the update check failed.
     */
    FAIL
}
